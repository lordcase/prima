<h3>Az adogatás</h3>
<img src="img/content/squash2.jpg" alt="Squash játékosok" class="right">
<p>Az elõször adogató játékos eldöntheti, hogy a jobb vagy a bal oldali adogatónégyzetbõl akar-e adogatni. Többnyire a jobb oldalit szokás választani jobbkezes ellenfél esetén, és a bal oldalit balkezes esetén, mert így a labdát az ellenfélnek fonákról kell visszaadnia, ami általában mindenkinek gyenge pontja.</p>

<p>Az adogatás akkor érvényes, ha az adogató legalább egyik lábának egy pontja a labda megütésének pillanatában az adogatónégyzeten belül van úgy, hogy nem érinti a négyzetet körülhatároló vonalak egyikét sem.</p>

<p>Az adogató feldobja a labdát a levegõbe, majd lepattanás elõtt elüti a fõfal felé. A labdának a fõfalon az adogatóvonal és a határvonal közé esõ területet kell érintenie, és úgy kell visszapattannia, hogy az éppen "használt" adogatónégyzettel ellentétes oldali hátsó pályanegyedben érjen padlót. A hátsó pályanegyed vagy más néven fogadónégyzet a keresztvonal, a félpályavonal, az oldalfal és a hátsó fal által határolt terület. (Ha a labda éppen a fogadónégyzet valamelyik határoló vonalán pattan, akkor az adogatás érvénytelen.)</p>



<p>A labda pattanhat a hátsó falon, az oldalfalon vagy akár mindkettõn, mielõtt padlót érne, de nem mehet a határvonalakon kívülre.<br />
A sikertelen adogatás neve fault: ebben az esetben a fogadó nyeri meg az adott labdamenetet.<br />
Az adogatás érvénytelen az alábbi esetekben:</p>
<ul>
<li>Az adogatónak legalább az egyik lába nem érintkezik a padlóval az adogatónégyzeten belül (lábhiba).</li>
<li>Az adogató beleér a labdába, miután az már lepattant a padlóra.</li>
<li>Az adogató felkészül és üt, de elvéti a labdát.</li>
<li>A labda nem pattan a fõfalon az adogatóvonal felett.</li>
<li>A labda az oldalfalon pattan elõször, és csak azután a fõfalon.</li>
<li>A labda az ellentétes fogadónégyzeten kívül esõ területen pattan a padlón.</li>
<li>A labda bármelyik falat a határvonalon kívül érinti.</li>
<li>Az adogató kétszer üt bele a labdába.</li>
<li>Az adogató összeütközik a labdával, miután az visszapattant a fõfalról</li>
