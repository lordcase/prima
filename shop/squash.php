<h2>A fallabda (squash) szabályai</h2>
<img src="img/content/squash1.jpg" alt="Squash játékosok" class="right">
<p>A fallabdát két játékos játssza egy 9,75 m hosszú és 6,4 m széles pályán.<br />
A szabályok a fair play-re és a  sportos eleganciára épülnek.<br />
A <strong>cipõ világos talpú</strong> legyen, a játékosoknak <strong>shortot és polót</strong> kell viselnie, hangosan szitkozódni illetve az ellenfelet ócsárolni nem szabad. (Klubunk házirendje több helyen is megtalálható a szabadidõközpontban.)</p>

<p>Amikor a játékosok a pályára lépnek, az elsõ játszma megkezdése elõtt öt perc "felmelegítõ" játék áll rendelkezésükre. Ez alatt a játékosok a pálya ellentétes térfelén állnak, és úgy ütik a fõfalnak a labdát, hogy az az ellenfél térfelén pattanjon. Két és fél perc után a játékosok térfelet cserélnek. Két célja van: egyrészt az, hogy felmelegítsék a labdát (ugyanis amikor a labda még hideg, kevésbé pattan), másrészt, a játékosok magukat is bemelegítsék néhány ütéssel.<br />
A játszmát csak meleg labdával lehet elkezdeni.<br />
Miután ötperces játékkal felmelegítették a labdát, megpörgetik az ütõt, hogy eldöntsék, melykõjük adogat elõször.</p>

<p>Az adogatás megkezdésével elkezdõdik a játék. Akkor nyeri meg a játékos a labdamenetet,</p>
<ul>
<li>ha ellenfele a tinre üti a labdát (fémmel borított, 0,45 m széles sáv a fõfal legalján);</li>
<li>ha a határvonalakon kívülre üti a labdát;</li>
<li>ha nem tud beleütni a labdába, mielõtt az másodszor is pattanna a padlón.</li>
</ul>
<p>A labdamenet folyamán a játékosok megüthetik a labdát közvetlenül a levegõbõl vagy az után, hogy egyet pattant a padlón. Az oldalsó és a hátsó falak használhatók arra, hogy a labdát visszajuttassák a fõfalra.</p>

<p>Mérkõzéseken többnyire az úgynevezett "hagyományos" vagy "nemzetközi" pontozást alkalmazzák, ami azt jelenti, hogy csak az a játékos szerezhet pontot, aki adogatott. Ha a menetet a másik játékos nyeri meg, akkor nem kap pontot, de megszerzi az adogatás jogát. Minden játszmát kilenc pontig játszanak.<br />
Az utóbbi években terjed az "amerikai" pontozási rendszer, ami azt jelenti, hogy a labdamenet bármelyik játékos megnyerheti, függetlenül attól, hogy ki adogatott. Ebben a szisztémában minden játszmát 15 pontig játszanak.</p>

<p>A mérkõzéseket csapatok is vívhatják egymás ellen. A csapatok három-öt játékosból állnak. Ilyenkor a csapattagok rangsor szerint mérkõznek egymással, vagyis elõször a legerõsebb játékosok mérik össze tudásukat, majd a második legjobbak és így tovább. Minden megnyert mérkõzés egy pontot ér.</p>

<p>A versenyeken számos versenyzõ, illetve csapat küzd egymás ellen. Általában egyenes kieséses rendszerben játszanak, ami azt jelenti, hogy az a játékos vagy csapat, amelyik vereséget szenved a mérkõzés során, kiesik a mezõnybõl. Léteznek csoportmérkõzéses rendszerû versenyek is: ilyenkor két elvesztett mérkõzés után esnek ki a játékosok, illetve csapatok. Harmadik lehetõség a körmérkõzéses verseny, ami azt jelenti, hogy mindenki mindenkivel megmérkõzik, és az a csapat vagy játékos nyeri a tornát, aki a legtöbb mérkõzést nyeri meg az összesítésben.</p>

