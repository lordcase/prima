<h3>Sparrowbag - ingyenes bemutató óra</h3>
<strong><p>2011. június 19. 15:00 óra</p></strong>

<strong><p>A sportszer elve:</p></strong>
<p>"Az izmokat gyakran osztályozzák aszerint, hogy stabilizációs vagy mozgató funkciót töltenek be. A mozgatóizom elsõsorban a mozgásért felelõs, mint például a m.biceps brachii, amely a könyökhajlítással történõ súly emelését végrehajtja. A stabilizátorok elsõdleges feladata a testben vagy mozgás közben egy adott ízületben biztosítani a stabilitást abból a célból, hogy a mozgatóizmok hatékonyan mûködjenek.<br />
A mindennapi élet követelményeit szem elõtt tartó funkcionális edzésekkel, olyan módon akarjuk edzeni az izmokat, ahogyan azt természetes funkciójuknak megfelel, ami azt jelenti, hogy a stabilizáló izmokat stabilizátorként, a mozgatóizmokat mozgatóként használjuk."<br />
Mark Vella - Fitnesz Anatómia, 2008-as kiadás</p>

<strong><p>Kialakítás és hatékonyság:</p></strong>
<p>A SPARROWBAG kialakításának elsõdleges szempontja volt, hogy a gyakorlatok végzése során azokat az izmokat mozgassa meg, amelyekre egy átlagos edzõtermi tréning során nem kerül sor. A testhez ergonomikusan illeszkedõ SPARROWBAG homokzsákon nem került kialakításra egyetlen fogást könnyítõ elem sem, melynek eredményeként a használata során kikényszeríti az elhanyagolt izmok fejlõdését. Az eszköz nehéz fogástechnikája nem teszi lehetõvé a kezek, és karok erõnlétéhez képest aránytalanul nagy súlyok mozgatását, ezáltal a túlterhelés veszélye kizárt. Formájából adódóan a klasszikus edzõtermi eszközökhöz képest kifejezetten nem balesetveszélyes.</p>

<p>A 12 alapgyakorlatból álló SPARROWBAG homokzsák edzés a teljes testet képes átmozgatni. A gyakorlatokon belüli ismétlésekkel és a gyakorlatsor ismétlésével tetszõlegesen növelhetjük az edzések intenzitását, ami avval bõvül ki, hogy a SPARROWBAG súlya is változtatható. Ezáltal a lehetõ legváltozatosabbá tudjuk tenni az edzésprogramunkat, meggátolva azt, hogy testünk hozzászokjon egy bizonyos megterhelési szinthez.</p>

<p>Regisztrálni a recepción lehet.</p>


