Rövid elõizzadást követõen - a vendégek verejtéktõl nedves bõrüket - a szaunamester által kiosztott finomított tengeri sóval dörzsölik be. A tengeri sót természetes testradírként használva megszabadulnak a megduzzadt és elhalt hámsejtektõl, ezáltal bõrük feszesebbé válik. A sóval sokkal aktívabbá tesszük verejtékmirigyeinket is, ezzel is fokozva a méregtelenítést. Rengeteg nyomelem és ásványi anyag található benne, amely a kitágult pórusokon keresztül könnyedén szívódik fel bõrünkbe. Újjávarázsolja, feszesíti a bõrt, csökkenti a cellulitisz tüneteit.<br />
A sós testradírozást követõen- a vendégek verejtéktõl nedves bõrüket - a szaunamester által csurgatott virágmézzel kenik be. A meleg hatására a méz cseppfolyóssá válik, a kitágult pórusokon keresztül gyorsan szívódik. Elõsegíti a bõr anyagcseréjét, hatásos bõrszennyezõdések, pattanások, kisebb hegek ellen. Hidratálja, táplálja és nyugtatja a bõrt. A szeánszok végére felüdült, kellemes tapintású, puha bõrrel távoznak a vendégek, arról nem is beszélve, hogy kellemes virágméz illatúan.</p>
