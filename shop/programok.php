<p><strong>Mi történik a szeánszok alatt?</strong></br />
Minden szeánsz ideje alatt a szaunamester bent tartózkodik a vendégekkel a szaunában, fokozatosan hevíti fel a kabint az aromás ráöntésekkel a kályhára, ezzel garantálva a vendégek aklimatizálódását. Igyekszik személyre szabott ventillációt biztosítani, a vendégek körül törölközõje segítségével kavarja a forró levegõt.<br />
